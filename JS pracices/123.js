// Declare variables
let firstName = "";
let age = 28;
const country = "India";

// Output the variables
console.log("Name:", firstName); // Output: Name: Alice
console.log("Age:", age);        // Output: Age: 28
console.log("Country:", country); // Output: Country: India

// Modify the value of a variable declared with let
age = 29;
console.log("Updated Age:", age); // Output: Updated Age: 29

