// Primitive Data Types
let name = "Nithya";          
let age = 25;               
let isHappy = true;         
let x;                     
let emptyValue = null;      
let bigNum = 12345678901234567890n; 
let id = Symbol("id");      


console.log("Name:", name);
console.log("Age:", age);
console.log("Is Happy:", isHappy);
console.log("X:", x);
console.log("Empty Value:", emptyValue);
console.log("Big Number:", bigNum);
console.log("ID:", id);

