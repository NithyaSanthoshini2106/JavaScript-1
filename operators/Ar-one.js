let a = 10;
let b = 3;

console.log("a =", a);
console.log("b =", b);

// Addition
console.log("a + b =", a + b); // 13

// Subtraction
console.log("a - b =", a - b); // 7

// Multiplication
console.log("a * b =", a * b); // 30

// Division
console.log("a / b =", a / b); // 3.333...

// Modulus (Remainder)
console.log("a % b =", a % b); // 1

// Exponentiation
console.log("a ** b =", a ** b); // 1000

// Increment
a++;
console.log("After a++, a =", a); // 11

// Decrement
b--;
console.log("After b--, b =", b); // 2
