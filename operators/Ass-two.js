// Basic Assignment
let x = 10;
console.log("x =", x);  // 10

// Add and assign (x = x + 5)
x += 5;
console.log("x += 5 →", x); // 15

// Subtract and assign (x = x - 3)
x -= 3;
console.log("x -= 3 →", x); // 12

// Multiply and assign (x = x * 2)
x *= 2;
console.log("x *= 2 →", x); // 24

// Divide and assign (x = x / 4)
x /= 4;
console.log("x /= 4 →", x); // 6

// Modulus and assign (x = x % 5)
x %= 5;
console.log("x %= 5 →", x); // 1

// Exponentiation and assign (x = x ** 3)
x **= 3;
console.log("x **= 3 →", x); // 1 (since 1^3 = 1)
